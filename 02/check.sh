#!/bin/bash
check() {
    [[ $# -ne 3 ]] && { echo "Нужно 3 аргумента"; return 1; }
    [[ ! $1 =~ ^[a-z]{1,7}$ ]] && { echo "Неверные символы папок"; return 1; }
    [[ ! $2 =~ ^[a-z]{1,7}\.[a-z]{1,3}$ ]] && { echo "Неверный шаблон файлов"; return 1; }
    [[ ! ${3,,} =~ ^([1-9][0-9]?|100)mb$ ]] && { echo "Неверный размер (1-100MB)"; return 1; }
    return 0
}
